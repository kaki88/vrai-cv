function rouge()
{
document.getElementById("gauche").style.backgroundColor = "#FF626F";
var divs = document.querySelectorAll("#date1, #date2, #date3, #date4");
[].forEach.call(divs, function(div) {
  div.style.color = "#FF626F";
});
}

